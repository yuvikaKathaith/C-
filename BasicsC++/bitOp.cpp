#include<iostream>
using namespace std;

int main(){
    int a=3;
    int b=1;
    cout<<"a&b : "<<(a&b)<<endl;
    cout<<"a|b : "<<(a|b)<<endl;
    cout<<"a^b : "<<(a^b)<<endl;
    cout<<"~a : "<<(~a)<<endl;
    cout<<"~b : "<<(~b)<<endl;
    cout<<"3<<1 : "<<(3<<1)<<endl;
    cout<<"3>>4 : "<<(3>>4)<<endl;

}